export function generateBunkerConditions() {
    const pro = BUNKER_FEATURES.pros[Math.floor(Math.random() * BUNKER_FEATURES.pros.length)];
    const con = BUNKER_FEATURES.cons[Math.floor(Math.random() * BUNKER_FEATURES.cons.length)];
    
    // Выбираем два разных предмета
    const item1 = BUNKER_FEATURES.items[Math.floor(Math.random() * BUNKER_FEATURES.items.length)];
    let item2 = BUNKER_FEATURES.items[Math.floor(Math.random() * BUNKER_FEATURES.items.length)];
    while(item1 === item2) {
        item2 = BUNKER_FEATURES.items[Math.floor(Math.random() * BUNKER_FEATURES.items.length)];
    }

    return `\n\n🏢 СОСТОЯНИЕ БУНКЕРА:\n✅ Преимущество: ${pro}\n❌ Проблема: ${con}\n🔎 На складе найдено: ${item1}, ${item2}`;
}

// Вспомогательная функция для случайного элемента массива
export function getRandomItem(array) {
    if (!array || array.length === 0) return "НЕТ ДАННЫХ";
    return array[Math.floor(Math.random() * array.length)];
}
